public void loadData(){
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        
        try {
            Connection konek = koneksi.getKoneksi();   
            Statement s = konek.createStatement();
            
            String sql = "SELECT * FROM data_nasabah order by nama_nasabah asc";
            ResultSet r = s.executeQuery(sql);
            
            while(r.next()) {
                Object[] o = new Object[8];
                o[0] = r.getString("id");
                o[1] = r.getString("nama_nasabah");
                o[2] = r.getString("nama_cicilan");
                o[3] = r.getString("total_cicilan");
                o[4] = r.getString("banyak_cicilan");
                o[5] = r.getString("cicilan_perbulan");
                o[6] = r.getString("ket");
                
                model.addRow(o);
            }
            r.close();
            s.close();
            
        } 
        catch(SQLException e) {
            
        }
        
    }
    
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int i = tabel1.getSelectedRow();
        if(i == -1) {
            return;
        }
        String id = (String) model.getValueAt(i,0);
        //String id = tid.getText();
        String nama = tnama.getText();
        String cicilan = tcicilan.getText();
        String total = ttotal.getText();
        String banyak = tbanyak.getText();
        String bulanan = tbulanan.getText();
        String ket = tket.getText();
        
        try {
            Connection konek = koneksi.getKoneksi();
            String sql="UPDATE data_nasabah SET nama_nasabah = ?, nama_cicilan = ?, total_cicilan = ?, banyak_cicilan = ?"
                    + ",cicilan_perbulan = ?, ket = ?  WHERE id = ? ";
            PreparedStatement p = konek.prepareStatement(sql);
            
            p.setString(1, nama);
            p.setString(2, cicilan);
            p.setString(3, total);
            p.setString(4, banyak);
            p.setString(5, bulanan);
            p.setString(6, ket);
            
            p.setString(7, id);
            
            p.executeUpdate();
            JOptionPane.showMessageDialog(null, "DATA BERHASIL DI UBAH", "Success", JOptionPane.INFORMATION_MESSAGE);
            tnama.setText("");
            tcicilan.setText("");
            ttotal.setText("");
            tbanyak.setText("");
            tbulanan.setText("");
            tket.setText("");
        }
        catch(SQLException e) {   
            JOptionPane.showMessageDialog(null, "Data Gagal Di Ubah...", "Error", JOptionPane.ERROR_MESSAGE);
            System.err.println(e.getMessage());
        }
        finally {
            loadData();
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

